// unkTable2 indexes into the three unkTable2_* arrays. Used by updateCallback46.
// The index pattern is: [table1, table2, table1, table2, table3, table2].
var unkTable2 = [6]*[130]uint8{
	// Note: entries 1, 3, 5 point to unkTable2_2 which is [128]uint8,
	// but we store as *[130]uint8 for uniformity — the opcode bounds-checks
	// the index anyway.
}

// unkTable2Ptrs provides safe access to the unkTable2 data by index.
// Returns the byte at position idx within the specified table.
func unkTable2Lookup(tableIdx, idx int) uint8 {
	switch tableIdx {
	case 0, 2:
		if idx >= 0 && idx < len(unkTable2_1) {
			return unkTable2_1[idx]
		}
	case 1, 3, 5:
		if idx >= 0 && idx < len(unkTable2_2) {
			return unkTable2_2[idx]
		}
	case 4:
		if idx >= 0 && idx < len(unkTable2_3) {
			return unkTable2_3[idx]
		}
	}
	return 0
}

